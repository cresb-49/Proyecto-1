    private void jButtonRealizarTransaccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRealizarTransaccionActionPerformed
        // TODO add your handling code here:
        String codigoProducto = this.jFormattedTextFieldCodigoDelProducto.getText();
        String NIT = this.jFormattedTextFieldNIT.getText();
        String nombreCliente = this.jFormattedTextFieldExistenciaDelProducto.getText();
        String codigoTienda = this.jComboBoxTiendas.getSelectedItem().toString();
        double precioProducto=0;
        double pagoEfectivo=0;
        double usoCredito=0;
        double creditoDisponible=0;
        double pagoMinimo=0;
        int unidadesAcomprar=0;
        //Se realizan las conversiones necesarias para aceptar los datos de entrada
        try {
            precioProducto = Double.parseDouble(jFormattedTextFieldPrecioDelPedido.getText());
            pagoEfectivo = Double.parseDouble(jFormattedTextFieldPagoEfectivo.getText());
            usoCredito = Double.parseDouble(jFormattedTextFieldCreditoAUsar.getText());
            creditoDisponible = Double.parseDouble(jFormattedTextFieldCreditoDisponible.getText());
            pagoMinimo = Double.parseDouble(jFormattedTextFieldPagoMinimo.getText());
            unidadesAcomprar = Integer.parseInt(jComboBoxUnidadeComprar.getSelectedItem().toString());
            
        } catch (Exception e) {
        }
        //Se comprueban los datos de entrada en para verficar que se haya seleccionado un producto
        if(!(codigoProducto.equals("")||precioProducto==0)){
            //verifica la disponibilidad del producto
            if(!(consultas.datosExistenciaProducto(codigoProducto, codigoTienda,this.baseDeDatos).endsWith("0"))){
                //Verifica que hayan los datos disponibles del cliente 
                if(!(nombreCliente.equals("")||NIT.equals(""))){
                    //verifica que el credito a utilizar sea una cantiad real
                    if(usoCredito>=0)
                    {
                        //Verifica el credito a utilizar en la compra
                        if(!(usoCredito>creditoDisponible)){
                            //Verifica que el pago total de la compra cumpla el minimo requerido
                            if(!((pagoEfectivo+usoCredito)<pagoMinimo)){
                                if((pagoEfectivo+usoCredito)>pagoMinimo){
                                    int respuesta = JOptionPane.showConfirmDialog(this, "Desea pagar mas del 25% minimo");
                                    if(respuesta==JOptionPane.YES_OPTION){
                                        ejecutarPedido();
                                    }
                                    else
                                    {
                                        JOptionPane.showMessageDialog(this, "Configure los pagos nuevamente\nNo se realizara la transaccion");
                                    }
                                }
                                else{
                                    //Acciones del pedido
                                    ejecutarPedido();
                                }
                            }
                            else
                            {
                                JOptionPane.showMessageDialog(this, "El pago no cumple el minimo de 25%\nNo se realizara la transaccion");
                            }
                        }
                        else
                        {
                            JOptionPane.showMessageDialog(this, "El credito del a usar supera el diponible\nNo se realizara la transaccion");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "El credito a usar no es correcto\nNo se realizara la transaccion");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "No a asignado el cliente\nNo se realizara la transaccion");
                }

            }
            else
            {
                JOptionPane.showMessageDialog(this, "No hay existencia del producto en la tienda seleccionada\nNo se realizara la transaccion");
            }
        }else{
            JOptionPane.showMessageDialog(this, "No a seleccionado ningun producto\nNo se realizara la transaccion");
        }
    }//GEN-LAST:event_jButtonRealizarTransaccionActionPerformed
    /**
     * Realiza la creacion del objeto pedido y lo introduce en la base de datos
     */
    private void ejecutarPedido(){
        String year = jComboBoxYear.getSelectedItem().toString();
        String mes = jComboBoxMes.getSelectedItem().toString();
        String dia = jComboBoxDias.getSelectedItem().toString();
        if((year.equals("AÃ±o"))&&(mes.equals("Mes"))&&(dia.equals("Dia"))){
            JOptionPane.showMessageDialog(this, "No a asignado una fecha a la transaccion");
        }
        else{
            //Variables para inicializar un pedido
            String fecha = year+"-"+mes+"-"+dia;
            String codigoPedido = "";
            String tiendaOrigen = jComboBoxTiendas.getSelectedItem().toString();
            String tiendaLlegada = this.codigoTiendaConSeccion;
            String cliente = jFormattedTextFieldNIT.getText();
            String producto = jFormattedTextFieldCodigoDelProducto.getText();
            int cantidad = Integer.valueOf(jComboBoxUnidadeComprar.getSelectedItem().toString());
            double total = Double.valueOf(jFormattedTextFieldPrecioDelPedido.getText());
            double efectivo = Double.valueOf(jFormattedTextFieldPagoEfectivo.getText());
            double credito = Double.valueOf(jFormattedTextFieldCreditoAUsar.getText());
            double anticipo = efectivo+credito;
            //Generacion del codigo de pedido del cliente
            codigoPedido=year+mes+dia+cliente;
            Pedido pedidoNuevo = new Pedido(codigoPedido, tiendaOrigen, tiendaLlegada, fecha, cliente, producto, cantidad, total, anticipo);
            //respuesta por si ocurre un error en el momento d ejcucion en la base de datos
            String respuesta = this.registrar.registroPedido(pedidoNuevo,this.baseDeDatos);
            if(!(respuesta.equals(""))){
                JOptionPane.showMessageDialog(this, respuesta);
            }
            else{
                JOptionPane.showMessageDialog(this, "Se ha registrado correctamente el pedido");
                descontarTienda();
                descontarCliente();
            }
            
        }
    }
    /**
     * Descuenta las existencia pedidadas de un producto en la tienda que lo expende
     */
    private void descontarTienda(){
        int cantidadVendida = Integer.valueOf(jComboBoxUnidadeComprar.getSelectedItem().toString());
        int cantidadOriginarl = Integer.valueOf(jFormattedTextFieldExistenciaDelProducto.getText());
        String codigoTienda = jComboBoxTiendas.getSelectedItem().toString();
        int nuevaCantidad = cantidadOriginarl-cantidadVendida;
        String codigoProducto = jFormattedTextFieldCodigoDelProducto.getText();
        Product productoModificar = new Product("", "", codigoProducto, nuevaCantidad, 0, "", 0, codigoTienda);
        this.modificarDatos.modificarExistencia(productoModificar,this.baseDeDatos);
    }
    /**
     * Descuenta la cantidad de credito del cliente si tuviera
     */
    private void descontarCliente(){
        String nitCliente = jFormattedTextFieldNIT.getText();
        double creditoDiponible = Double.valueOf(jFormattedTextFieldCreditoDisponible.getText());
        double creditoUsado = Double.valueOf(jFormattedTextFieldCreditoAUsar.getText());
        double nuevoCredito = creditoDiponible-creditoUsado;
        this.modificarDatos.modificarCreditoCliente(nitCliente, String.valueOf(nuevoCredito),this.baseDeDatos);
    }
